Rafael Urrusti Alonso<br><br>
Por Pierre-Marc René<br><br><br>
Rafael Urrusti Alonso es descendiente de una familia que huyó de España en 1939 durante la Guerra
Civil y la dictadura de Francisco Franco. A sus 61 años y al cumplirse 80 aniversario del exilio español,
el reconocido artista reclama que se declare ‘ejemplo de la humanidad, al embajador Gilberto
Bosques, quien organizó por orden del presidente Lázaro Cárdenas la acogida de decenas de miles
de refugiados españoles y europeos en México.<br><br>
“Lo que significan Gilberto Bosques y la política de México del presidente Lázaro Cárdenas es la
diferencia entre la vida y la muerte para decenas de miles de personas. Yo siempre he tenido la
inquietud de honrarlo más como se merece, si es que hay manera de medir eso. De salvar a
tantísimas miles de vida, no necesariamente refugiados españoles, porque cualquiera que fuera un
ser humano y que necesitara refugio, acogida y protección (…) Estaban México, Gilberto Bosques y
Lázaro Cárdenas”, declara el flautista.<br><br>
